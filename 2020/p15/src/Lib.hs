play' :: Int -> Map Int Int -> Int -> [Int] -> [Int]
play' i !m last (h:t) = h : play' (i + 1) (Map.insert last i m) h t
play' i !m last [] =
    let next =
            case Map.lookup last m of
                Just i' -> i - i'
                Nothing -> 0
    in next : play' (i + 1) (Map.insert last i m) next []

play :: [Int] -> [Int]
play = play' 0 mempty (-1)

